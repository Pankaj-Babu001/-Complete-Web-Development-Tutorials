// ANATOMY OF FOR LOOP
console.log("--- For Loop Anatomy ---");
console.log("for (initialization; condition; final-expression) { code }");
console.log("\n1. Initialization: runs once before loop starts");
console.log("2. Condition: checked before each iteration");
console.log("3. Code block: executes if condition is true");
console.log("4. Final-expression: runs after each iteration");

console.log("\nExample: Count to 5");
for (let i = 1; i <= 5; i++) {
  console.log(`  Iteration ${i}: i = ${i}`);
}

